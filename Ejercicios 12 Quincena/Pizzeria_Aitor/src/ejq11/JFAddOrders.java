    private boolean isRadioBtnExtraSelected() {
        boolean ok = true;

        if (this.checkBoxExtras.isSelected()) {
            if (!this.radioBtnExtra1_1.isSelected() && !this.radioBtnExtra1_2.isSelected() && !this.radioBtnExtra1_3.isSelected() && !this.radioBtnExtra1_4.isSelected() && !this.radioBtnExtra1_5.isSelected()) {
                if (!this.radioBtnExtra2_1.isSelected() && !this.radioBtnExtra2_2.isSelected() && !this.radioBtnExtra2_3.isSelected() && !this.radioBtnExtra2_4.isSelected() && !this.radioBtnExtra2_5.isSelected()) {
                    if (!this.radioBtnExtra3_1.isSelected() && !this.radioBtnExtra3_2.isSelected() && !this.radioBtnExtra3_3.isSelected() && !this.radioBtnExtra3_4.isSelected() && !this.radioBtnExtra3_5.isSelected()) {
                        ok = false;
                    } else {
                        this.lblErrorExtra.setText("");
                    }
                } else {
                    this.lblErrorExtra.setText("");
                }
            } else {
                this.lblErrorExtra.setText("");
            }
        }

        return ok;
    }

    private String getSelectedButtonText(javax.swing.ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getText();
            }
        }

        return null;
    }

