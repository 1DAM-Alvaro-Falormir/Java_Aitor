    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        boolean ok = true;

        if (this.txtNombreCliente.getText().isEmpty()) {
            this.lblErrorNombre.setForeground(Color.red);
            this.lblErrorNombre.setText("El nombre no puede estar vacio");
            ok = false;
        } else {
            ok = true;
            this.lblErrorNombre.setText("");
        }
        if (this.txtApellidosCliente.getText().isEmpty()) {
            this.lblErrorApellido.setForeground(Color.red);
            this.lblErrorApellido.setText("El apellido no puede estar vacio");
            ok = false;
        } else {
            ok = true;
            this.lblErrorApellido.setText("");
        }
        try {
            if (Utilidades.checkDni(this.formatTxtDni.getText())) {
                this.lblErrorDni.setText("");
                ok = true;
            }
        } catch (ExcepcionPropia e) {
            this.lblErrorDni.setForeground(Color.red);
            this.lblErrorDni.setText(e.getMessage());
            ok = false;
        } catch (NumberFormatException e) {
            this.lblErrorDni.setForeground(Color.red);
            this.lblErrorDni.setText("El DNI no puede estar vacio");
            ok = false;
        }
        try {
            if (Utilidades.checkRango(Integer.parseInt(this.txtCantidad.getText()))) {
                this.lblErrorCantidad.setText("");
                ok = true;
            }
        } catch (NumberFormatException e) {
            this.lblErrorCantidad.setForeground(Color.red);
            this.lblErrorCantidad.setText("ERROR: La cantidad tiene que ser numerica");
            ok = false;
        } catch (ExcepcionPropia e) {
            this.lblErrorCantidad.setForeground(Color.red);
            this.lblErrorCantidad.setText(e.getMessage());
            ok = false;
        }
        ok = isRadioBtnExtraSelected();
        if (!ok) {
            this.lblErrorExtra.setForeground(Color.red);
            this.lblErrorExtra.setText("ERROR: Si elije un extra, debe seleccionar al menos un ingrediente.");
        } else {
            this.lblErrorExtra.setText("");
        }
        if (ok) {
            this.btnGuardar.setEnabled(true);
        } else {
            this.btnGuardar.setEnabled(false);
        }

    }//GEN-LAST:event_btnValidarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre, apell, dni, nomPizza;
        int cantidad, extra1 = 0, extra2 = 0, extra3 = 0;
        boolean extra, insertado;
        Pedido pedido;

        nombre = this.txtNombreCliente.getText();
        apell = this.txtApellidosCliente.getText();
        dni = this.formatTxtDni.getText();
        nomPizza = getSelectedButtonText(this.btnGroupNombre);
        cantidad = Integer.parseInt(this.txtCantidad.getText());
        extra = this.checkBoxExtras.isSelected();
        if (extra) {
            if (this.btnGroupExtra1.isSelected(btnGroupExtra1.getSelection())) {
                if (getSelectedButtonText(this.btnGroupExtra1) != null) {
                    extra1 = Integer.parseInt(getSelectedButtonText(this.btnGroupExtra1));
                }
            }
            if (this.btnGroupExtra2.isSelected(btnGroupExtra2.getSelection())) {
                if (getSelectedButtonText(this.btnGroupExtra2) != null) {
                    extra2 = Integer.parseInt(getSelectedButtonText(this.btnGroupExtra2));
                }
            }
            if (this.btnGroupExtra3.isSelected(btnGroupExtra3.getSelection())) {
                if (getSelectedButtonText(this.btnGroupExtra3) != null) {
                    extra3 = Integer.parseInt(getSelectedButtonText(this.btnGroupExtra3));
                }
            }
        }

        if (extra) {
            pedido = new Pedido(nombre, apell, dni, nomPizza, cantidad, extra, extra1, extra2, extra3);
        } else {
            pedido = new Pedido(nombre, apell, dni, nomPizza, cantidad, extra);
        }
        insertado = listaP.addPedido(pedido);
        if (insertado) {
            JOptionPane.showMessageDialog(this, "Pedido insertado");
        } else {
            JOptionPane.showMessageDialog(this, "No se ha podido insertar el pedido");
        }
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

